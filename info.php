<p.class ="right">
Cornwall has a temperate Oceanic climate and as a result of its southerly latitude and the influence of the Gulf Stream has the mildest and sunniest climate in the United Kingdom.</P>
 <p.class ="right">The average annual temperature in Cornwall ranges from 11.6 °C (53 °F) on the Isles of Scilly to 9.8 °C (52 °F) in the central uplands. Winters are amongst the warmest and shortest in the country due to the southerly latitude and moderating effects of the warm ocean currents. The resultant early spring and long growing season means that Cornwall is home to an abundant array of quality produce, and also that we start to see the early crops of vegetables and flowers before the rest of the country.</p>
<p.class ="right">  Frost and snow are very rare. The surrounding sea and its southwesterly position mean that Cornwall's weather is relatively changeable.<br/></p>
<p.class ="right">Cornwall is one of the sunniest areas in the UK, with over 1541 hours of sunshine per year, with the highest average of 7.6 hours of sunshine per day in July. The moist, mild air coming from the south west brings higher amounts of rainfall than in eastern Great Britain, at 1051 to 1290 mm (41.4 to 50.8 in) per year, however not as much as in more northern areas of the west coast. This rain along with all that lovely sun helps to make our county so green and beautiful.</p>
<p.class ="right"> The Gulf Stream, bringing warm air from the Caribbean north-east toward Europe, makes Cornwall's weather distinctly milder than other places in the world at the same latitude, such as Newfoundland. Also due to the Gulf Stream, Cornwall has the UK's only area of sub-tropical climate. This sub-tropical nature has resulted in a number of botanical gardens, such as <a href="http://www.trebahgarden.co.uk/" target="_blank" title="Link to Trebah Gardens - Opens a new window.">Trebah</a> and the Lost Gardens of <a href="http://www.heligan.com/" target="_blank" title="Link to Heligan - opens in new window.">Heligan</a>.</p>
<p.class ="right">Pendennis Point in Falmouth is the warmest place on mainland Great Britain, with an average temperature of 11.4°C (52.5°F).
Extreme temperatures in Cornwall are particularly rare.
