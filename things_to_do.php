<p align="center">
    <strong><u>Things to do</u></strong>
</p>
<p>
    Here are just a few things that we really recommend considering.
</p>
<p id  = "walking" align="center">
    <strong><u>Walking</u></strong>
</p>
<p>
    Lots of walks are accessible directly from the cottage but an OS map is
    needed to make the most of these. Please keep our farmers happy by keeping
    to the footpaths marked by yellow arrows and closing any gates behind you.
    Please remember to keep dogs well under control during lambing season and
    on a lead around sheep and lambs.
</p>
<p>
    The <strong>South West Coast Path</strong> is famous for its amazing
    scenery and we recommend the stretch between Looe and Polperro (just over 5
    miles), buses and boat trips connect the two ports so a one-way walk can be
    enjoyed without worrying about getting back to the car.
</p>
<p>
    <strong>Bodmin Moor</strong>
    is around 10 minutes away and has many walking opportunities, we
    particularly recommend that you consider a walk to ‘The Hurlers’ standing
    stones from the village of Minions (worth a trip for a photo next the
    village sign), Golitha Falls, Sibleyback Lake. If you are on the moor from
    May to October please be tick aware.
</p>
<p>
    <strong>Kit Hill Country Park</strong>
    was given by the Duchy of Cornwall to the people of Cornwall to celebrate
    the birth of Prince William in 1984. It’s a rugged granite hilltop that
    rises to over 1000 feet above sea level and on a clear day you can see both
    the north and south coasts of Cornwall. Access is from the east, off the
    link road between Monkscross on the B3257 and the top of Silver Valley on
    the A390.
</p>
<p>
    <strong>Accessible walks</strong>
    that are suitable for buggies or wheelchairs, or for anyone who fancies an
    easier stroll can be found at Cardinham Wood (has 4 multi use trails, a
    play area and a cafe), Seaton Nature Reserve (next to the beach car park),
    Saltram Park and Gardens, Cotehele Quay (flat footpath through the woods to
    the weir), Plymouth sea front and Hoe, Siblyback Lake, the multi-use trail
    at Mount Edgcumbe Park, and the historical trail at Wheal Martyn.
</p>
<p>
    <strong>The Tamar Valley</strong>
    – an area of outstanding natural beauty and mining heritage. Many walks
    with lots of accessible and multi use trails. For details got to
    <a href="http://www.tamarvalley.org.uk/explore/access/walking/">
        http://www.tamarvalley.org.uk/explore/access/walking/
    </a>
</p>
<p>
    Or visit the Tamar Valley Centre in Drakewalls, near Gunnislake, PL18 9FE
</p>
<p>
Further afield we also love <strong>Lydford Gorge </strong>on Dartmoor (    <strong>EX20 4BH</strong>) with a beautiful, challenging gorge walk some of
    which is along rock hewn paths right next to the river, with the White Lady
    waterfall and swirling Devil’s Cauldron (shorter, easier walks are also
    possible).
</p>
<p>
    <strong><u></u></strong>
</p>
<p id = "beaches" align="center">
    <strong><u>Beaches and Coast</u></strong>
</p>
<p>
    There is a guide to beaches in SE Cornwall
</p>
<p>
    <a href="https://www.cornwall-beaches.co.uk/south-east/">
        https://www.cornwall-beaches.co.uk/south-east/
    </a>
</p>
<p>
    <a href="https://www.cornwallbeachguide.co.uk/secornwall/secornwall.htm">
        https://www.cornwallbeachguide.co.uk/secornwall/secornwall.htm
    </a>
</p>
<p>
    Dog friendly beach list here (please be aware that some beaches only allow
    dogs out of high season)
</p>
<p>
    https://www.cornwall-beaches.co.uk/dog-friendly
</p>
<p>
Our nearest beaches are <strong>Seaton</strong> and    <strong>Downderry</strong> both 6 miles away, Seaton is particularly good
    for families and is lifeguarded at high season. Seaton has a countryside
    park with a buggy-friendly walk, pentanque court, and playground and there
    is a buggy-friendly path along the edge of the beach heading away from the
    car park. Both Seaton and Downderry have a choice of places to buy food and
    drinks. The beaches here have grey sand, formed from the granite and slate
    from the moor.
</p>
<p>
    We recommend a drive around the <strong>Rame Peninsula</strong>; it is
    known as Cornwall’s forgotten corner and has some of the most impressive
    coastal scenery. The peninsula is also worth visiting for
    <strong>
        Antony House, Port Eliot, Mount Edgcumbe Country Park, Kingsand
    </strong>
    and<strong> Cawsand, Rame Church </strong>and<strong> Rame Head.</strong>
</p>
<p>
    The Rame Peninsula is also home to <strong>Whitsand Bay</strong> where the
beaches (<strong>Tregantle, Tregonhawke, Sharrow Point </strong>and    <strong>Freathy</strong>) are very popular with surfers, body boarders and
    local teenagers but are only accessible down a steep cliff path, Tregantle
    being the least steep. The steep path puts off a lot of visitors and so
    there is a good chance of enjoying Whitsand Bay’s three miles of sand with
    a limited number of other people. There are cafes on the cliff and the
    beach at Tregonhawke. At high tide many of the beach areas can become cut
    off so please be aware of tide times before you go (general high tide times
    are shown by area on the local weather forecast).
</p>
<p>
    <strong>Looe</strong>
    is worth a visit, there is a range of shops in East Looe, and it’s a great
    spot for crabbing and to watch the fishermen unload their catch if you time
    it right. Looe Harbour can be a good place to spot grey seals. The beach is
    grey sand and close to Banjo pier and the lifeboat station, it is accessed
    by walking all the way through the town. Just round the headland of West
    Looe, <strong>Hannafore Point</strong> beach is a rocky shore which is
    great for rock-pooling. Looe gets busy in the holidays and parking can be
difficult but you can also reach Looe by taking the    <strong>scenic train line from Liskeard</strong>, which is reasonably
    priced with parking at the station. Lots of boat trips can be taken from
    Looe for mackerel fishing, glass bottom boat rides or a trip to the marine
    nature reserve of St George’s Island (also known as Looe Island).
</p>
<p>
    <strong>Polperro</strong>
    is a step back in time; the fishing port hasn’t changed much over the
    years, with its narrow lanes and wonky houses. It’s a walk from the car
    park but there is also a horse and cart or the village tram. There are
    plenty of pubs and tea rooms – the Blue Peter is our favourite pub here and
    the food they serve is good. Parking can be expensive here, it accessible
    by bus from Looe.
</p>
<p>
    <strong>Fowey</strong>
    has a range of interesting shops and lots of cafes and pubs. It was home to
    author Daphne du Maurier and there are plenty of literary themed activities
    (visit Fowey Tourist Information Centre). You can also catch a ferry from
    here to Polruan or Mevagissy (and boat trips to many other places) and
    there is a family-friendly beach named Readymoney Cove.
</p>
<p>
    <strong>Polkerris</strong>
    for a good family beach and watersports.<strong></strong>
</p>
<p align="center">
    <strong><u> </u></strong>
</p>
<p id = "gardens" align="center">
    <strong><u>Gardens and History</u></strong>
</p>
<p>
    <a href="https://www.greatgardensofcornwall.co.uk/">
        https://www.greatgardensofcornwall.co.uk/
    </a>
</p>
<p>
    https://www.visitcornwall.com/things-to-do/gardens
</p>
<p>
    <strong>Eden Project</strong>
    – of course we must recommend the excellent Eden Project (PL24 2SG) which
    is around a 30–40 minute drive away. Eden has many new attractions every
    year and a full events programme. It is on the expensive side but you can
    often acquire vouchers and offers.
</p>
<p>
    <strong>Cotehele House and Quay </strong>
    (PL12 6TA) is a Tudor manor little changed since it was completed in 1539,
    with gardens leading down to the Tamar river. It can also be reached by the
    Calstock Ferry. The watermill has a blacksmith, carpenter, wheelwright and
    saddler.
</p>
<p>
    <strong>The Lost Gardens of Heligan</strong>
    (PL26 6EN) – gives a fascinating view of a grand Cornish Victorian garden
    which was “lost” when the gardening staff all joined up for the First World
    War. There are boardwalks through banana palms and bamboo, subtropical
    jungle, woodland sculptures, lots of wildlife and a tea room.
</p>
<p>
    <strong>Restormel Castle</strong>
    (PL22 0EE) is a ruin just a mile from Lostwithiel, it’s a good spot for
    picnics and great for children to explore. Lostwithiel is also an
    interesting medieval town, and ancient capital of Kernow, with many antique
    shops. It’s also where Prince Charles has his Cornish home.
</p>
<p>
    <strong>Lanhydrock </strong>
    (PL30 5AD) is a magnificent country house, massive garden and estate; it
    was described by one of our guests as the best National Trust property he’d
    ever visited. Great Cafe, play area, bike hire and family friendly cycle
    paths.
</p>
<p>
    <strong>Morwellham Quay</strong>
    (PL19 8JL) is a historic port, farm, and railway and copper mine. Home of
    the BBC’s <em>Edwardian Farm</em>.
</p>
<p>
    <strong>Plymouth Barbican</strong>
    is a cobbled, Tudor area with independent shops and lots of eating/drinking
    places on the harbourside. Within this small area of the city there is also
the historic <strong>Elizabethan House</strong> and<strong>Merchant’s House</strong> and    <strong>Plymouth Gin Distillery</strong> – they are all well worth a visit
    and were the only part of Plymouth to escape devastating World War Two
    bombings.
</p>
<p>
    <strong>Bodmin Moor</strong>
    is just 5 miles away and has many places to see, great walking, climbing
    and wild swimming – just a few of them are Golitha Falls Nature Reserve,
    Carnglaze Caverns, Cardinham Woods, many ancient standing stones such as
    The Cheesewring and The Hurlers (head for the village named Minions) or the
    Neolithic Fort of Helman Tor.
</p>
<p>
    <strong>Bodmin and Wenford Railway</strong>
    – six-mile full-sized steam railway from Bodmin General Station to Boscarne
    Junction where it links with the Camel Trail (an 18-mile cycle trail
    following a disused railway track alongside the River Camel that leads
    ultimately to Padstow).
</p>
<p>
    <strong>Wheal Martyn China Clay Country Park – </strong>
    China clay museum, 26-acre Country Park, children’s challenge trail and
    play area.
</p>
<p>
    <strong>Bodmin Jail</strong>
    (PL31 2RN) – A fascinating historical jail that is both educational and
    fun, especially for those who like a bit of spine-tingling! Re-opened in
    October 2020 after extensive refurbishment.
</p>
<p>
    <strong>Pencarrow House and Garden</strong>
    (PL30 5AG) – The family home of the Molesworth-St Aubyns for almost 500
    years. On the edge of Bodmin Moor with Grade II listed garden, Iron Age
    fort, play areas, café and gift shop. 50 acres to explore and dog friendly.
</p>
<p align="center">
    <strong><u> </u></strong>
</p>
<p id = "animal" align="center">
    <strong><u>Animal Attractions</u></strong>
</p>
<p>
    <strong>The Monkey Sanctuary</strong>
    (PL13 1NZ) has a spectacular position above the sea near Looe; this is a
    working sanctuary and not a zoo. The monkeys have all been rescued and many
    of them show the signs of their past. The keepers give regular talks but it
    probably isn’t suitable for very young children as noise can frighten the
    monkeys. The sanctuary has a vegetarian Treetop Cafe.
</p>
<p>
    <strong>National Marine Aquarium</strong>
    (Plymouth Barbican) multi-level, zoned aquarium with over 4000 creatures
    from 400 species, it features Britain’s deepest tank and over 70 sharks.
</p>
<p>
    <strong>Newquay Zoo</strong>
    (TR7 2NN) – small sized wildlife park operated by a conservation charity.
</p>
<p>
    <strong>Paignton Zoo </strong>
    (TQ4 7EU) – further away than Newquay but this is really good large zoo
    with more to see than could fill just one day.
</p>
<p>
    <strong>Dairyland Farm World (</strong>
    TR8 5AA) – lots of animals, lots of play equipment and a working dairy farm
</p>
<p>
    <strong>Screech Owl Sanctuary </strong>
    (TR9 6HP) – Close encounters with owls<strong></strong>
</p>
<p id = "active" align="center">
    <strong><u>Active/Adventures</u></strong>
</p>
<p>
    All suitable for families depending on ages/swimming ability.
</p>
<p>
    <strong>Adrenalin Quarry</strong>
    (PL14 3PJ), just up the road has the UK’s fastest zip wire descending over
    a flooded quarry, kart tracks for all ages, aqua park, coasteering and a
    giant swing.
</p>
<p>
    <strong>Adventure Fit South West </strong>
    in Looe (PL13 2BX) climbing wall, kayak, canoe and Stand Up Paddleboard
    (SUP) trips tel. 01503 262000
</p>
<p>
    <strong>Horse riding</strong>
    – try Great Trethew Trekking (PL14 3PY) (tel. 07791174104) who can arrange
    beach rides, Polmartin Farm (PL14 4RE) (tel. 01503 220428) or to ride on
    Bodmin Moor try either Lower Tokenbury Equestrian Centre (PL14 5AR) (tel.
    01579 362747) or there is also TM International School of Horsemanship
    (PL14 5BP) (tel. 01579 362895). Poldark rides with Hallagenna Riding (tel
    01208 851500)
</p>
<p>
    <strong>Surfing and Coasteering</strong>
– Adventure Bay Surf School at Tregonhawke Beach, Whitsand Bay tel.<strong>07722 913 206</strong><strong> , </strong>    <strong>Discovery Surf School, Bigbury https://discoverysurf.com/</strong>
</p>
<p>
    <strong>Kayaking and Watersports</strong>
    River Kayaking on the Tamar with Canoe Tamar (tel 01822 833409), part of
    Tamar Trails, Stand Up Paddle board and Kayaks at Blue Dot Adventures at
    Mount Edgcumbe Country Park, on the River Fowey with Encounter Cornwall
    (Tel. 01208 871 066) or Fowey Kayak Experience (tel. 01726 833627),
    Cornwall Kayaking at St Austell, Porthpean Outdoor Education Centre, St
    Austell. Polkerris beach https://polkerrisbeach.com/
</p>
<p>
    <strong>Flowrider at Retallack resort</strong>
    – Body boarding and surfing but not in the sea! Suitable for children age
    5+ between Bodmin and Newquay.
</p>
<p>
    <strong>Cycling – </strong>
    There are many cycling routes throughout Cornwall see:
</p>
<p>
    <a href="http://www.cornwall.gov.uk/default.aspx?page=17685">
        http://www.cornwall.gov.uk/default.aspx?page=17685
    </a>
</p>
<p>
    The multi use <strong>Tamar trails</strong> mentioned in the walking
    section above.
</p>
<p>
    <strong>Cardinham Woods</strong>
    – has had a lot of cycle trail development and now boasts a blue (moderate)
    grade 12km off –road/mountain biking trail named the Bodmin Beast, there
    are two red-grade loops off this trail suitable for proficient cyclists
    named Dialled-in Dave and Hell’s Teeth
</p>
<p>
    <strong>Lanhydrock</strong>
    - bike hire, family friendly and intro to off road cycling trails and some
    sligtly more challenging routes. Outside of the house so no payment to
    access. Also has a nice cafe and children's play area.
</p>
<p>
    There are many bike hire places in the area, many also hire trailers,
    electric or adapted bikes.
</p>
<p>
    <strong>Hangloose at The Eden Project </strong>
    – zip wire over the Eden Quarry, free fall, giant swing
</p>
<p id = "other" align="center">
    <strong><u>Others</u></strong>
</p>
<p>
    <strong>St Austell Brewery Visitor Centre and tours</strong>
    (PL25 4BY) – an interactive museum and tour (tour only accepts children
    over 8) teaching you all about brewing and the history of St Austell
    Brewery, ending with tastings.
</p>
<p>
    <strong>Sterts Theatre and Arts Centre</strong>
    (PL14 5AZ) – lots of fantastic arts and theatre events in a magical setting
    on the edge of the moor. Built in a natural amphitheatre covered by a giant
    tent.
</p>
<p>
    <strong>Camel Valley Vineyard</strong>
    (PL30 5LG) – Cornwall’s award winning vineyard, tours, tastings and shop.
    Choose from an early evening tour with tasting sessions or an afternoon
    tour with a glass of wine and soft drinks/concession prices for children!
</p>
